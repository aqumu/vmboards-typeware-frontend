	<h3>VM35 Update #1</h3>
	<h5>Since the first IC quite a lot has changed in the design.</h5>
	<h5>
		Firstly, we've made away with the sandwich mount in order to replace it with its more popular
		sibling, the gasket mount. While this decision may come off as us chasing trends, we believe
		it's truly the better option, considering the adaptive use case and the amount of free space
		left in the case itself. This has also allowed us to hide the previously visible screws on the
		outside.
	</h5>
	<h5>
		Secondly, we've built in support for PCB mount stabilizers. However, we had to make the hard
		choice to cut the support for plate mount stabilizers. Once again, it was made for the sake of
		the design itself, as that allows changing form factors without the use of many different
		plates.
	</h5>
	<h5>
		And finally, we have made a fair amount of changes internally, including the use of a
		daughterboard, better wiring and support for Poron foam.
	</h5>
	<h5>We hope you like all these changes.</h5>
	<h5>We have also made more renders. Take a look!</h5>
